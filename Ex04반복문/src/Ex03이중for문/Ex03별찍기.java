package Ex03이중for문;

public class Ex03별찍기 {

	public static void main(String[] args) {

		// ******
		// ******
		// ******
		// ******
		// ******
		// 이중 for문 사용

		for (int j = 1; j <= 5; j++) {
			// j: 1 2 3 4 5
			for (int i = 1; i <= 5; i++) {
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
